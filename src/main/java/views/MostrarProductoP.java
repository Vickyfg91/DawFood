    private void actualizarPrecioTotal() {
        try {
            final int porcentaje = 100;
            int fila = jTable1.getSelectedRow();
            if (fila == -1) {
                throw new IllegalArgumentException("Debe seleccionar un producto para calcular el precio.");
            }

            int idProducto = (int) jTable1.getValueAt(fila, 0);
            int stock = (int) jTable1.getValueAt(fila, 4);

            productoSeleccionado = listaComida.stream()
                    .filter(p -> p.getIdProducto().equals(idProducto))
                    .findFirst()
                    .orElse(null);

            int cantidad = (int) jSpinnerCantidad.getValue();
            if (cantidad < 1) {
                throw new IllegalArgumentException("Debe seleccionar una cantidad en positivo. Si desea borrar el producto vaya al carrito.");
            }
            if (cantidad > stock) {
                throw new IllegalArgumentException("No queda suficiente producto de lo que has elegido. M치ximo " + stock);
            }

            double precioTotal = (((productoSeleccionado.getPrecioProducto() * productoSeleccionado.getIvaProducto()) / porcentaje) + productoSeleccionado.getPrecioProducto()) * cantidad;
            jTextFieldPrecio.setText(String.format("%.2f", precioTotal));

        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "DawFood", JOptionPane.ERROR_MESSAGE);
        }
    }

